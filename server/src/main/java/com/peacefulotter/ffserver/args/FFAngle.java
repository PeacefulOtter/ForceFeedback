package com.peacefulotter.ffserver.args;

public class FFAngle
{
    private float angle;

    public FFAngle()
    {
        this.angle = 0;
    }

    public FFAngle( float angle )
    {
        this.angle = angle;
    }

    public float getAngle()
    {
        return angle;
    }

    public void setAngle( float angle )
    {
        this.angle = angle;
    }
}
